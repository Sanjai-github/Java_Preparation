package com.sanjai.loops;

public class For_Loop {
    public static void main(String[] args) {
        /*
        for loop syntax :
        for (initialisation , condition , Increment/ Decrement){
        statement
        }
        print all numbers for 1 - 10
        */
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
